		</h2>
		<p class="text-bold text-light-green padding-left-5 padding-right-5">As the nation battles an opioid epidemic, clinicians across the country are forced to reconsider the abuse potential of a nerve pain medication - gabapentin.
		</p>
		<p class="padding-left-5 padding-right-5">
		Gabapentin, also prescribed as Neurontin, is a nerve pain medication used to treat a variety of conditions. Most notably, it is prescribed to treat seizures and neuropathic pain. It is so commonly used in the treatment of neuropathy that it has surpassed oxycodone in volume of doses prescribed. It’s prescribed for a broad swathe of off-label uses including treatment of anxiety, insomnia, bipolar disorder, restless leg syndrome, alcoholism, migraine, hot flashes, fibromyalgia, and many other conditions.

		</p>
		<p class="padding-left-5 padding-right-5">
But its frequent usage is not without its downsides. Gabapentin abuse does not get the attention it deserves from the healthcare industry. Even when prescribed and taken as directed by the company, it is possible that patients may experience ataxia, peripheral edema (swelling), drowsiness, and renal toxicity. However, patients who abuse the medication. When combined with opioids, gabapentin can provide a euphoric effect and mitigate opioid withdrawal symptoms. It can even provide a ‘boost of energy,’ as some patients and recreational users describe, or a ‘marijuana-like high’ as described by others.


		</p>
		<p class="padding-left-5 padding-right-5">
Gabapentin prescriptions are not slowing down! An IMS Health study noted that prescriptions for gabapentin have increased 47% in frequency from 2011 to 2015. In 2015, the same study notes, 57 million prescriptions for gabapentin were written nationwide. 

		</p>
		<p class="padding-left-5 padding-right-5">
The way it’s used has been changing which may be driving the increasing volume of prescriptions. Gabapentin is not a controlled medication. The way this regulation was decided was based on the fact that it used to be use as a monotherapy, but it now is used alongside several other medications (most notably opioids!). Due to its radical shift regarding in the past decade  how it’s used, it’s prompting questions about how to classify the medication.
