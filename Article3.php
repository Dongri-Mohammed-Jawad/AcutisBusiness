Most importantly, this study highlights that the nature of the opioid crisis is demographically quite different from state to state. The study looked at the five most-populous cities in the country: Chicago, Houston, Los Angeles, New York, and Philadelphia. It discovered the following trends about each city:

		</p>
		<p class="padding-left-5 padding-right-5">
			<ul class="font-20 margin-left-5">
				<li>Out of these cities, Philadelphia has the highest proportion of claims with opioid-related diagnoses in comparison to total claims across the state.</li>
				<li>Claims with opioid-related diagnoses in Los Angeles increased 31,897% from 2007 to 2016.</li>
				<li>San Antonio, Texas holds only 5% of Texas’ population but is responsible for 66% of claims with opioid-related diagnoses.</li>
				<li>In Illinois and Pennsylvania, males are more likely to file for claims with opioid-related diagnoses, although the gap narrows above age 50.</li>
				<li>In Acutis Diagnostics’ home state of New York, the researchers found that New York City constituted 43% of the population but only 13% of the claims with opioid-related diagnoses in the state.</li>


			<ul>
